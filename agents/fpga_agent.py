def _make_doxygen_header(
    *, filename: str, brief: str, project_name: str, module_name: str,
    clock_mhz, comment_token: str = "//",
) -> str:
    """Build a 7-line SPDX + Doxygen banner for generated HDL/XDC files."""
    c = comment_token
    lines = [
        f"{c} SPDX-License-Identifier: MIT",
        f"{c} SPDX-FileCopyrightText: Auto-generated by Silicon to Software (S2S) v2",
        f"{c} @file        {filename}",
        f"{c} @brief       {brief}",
        f"{c} @project     {project_name}",
        f"{c} @module      {module_name}",
        f"{c} @clock       {clock_mhz} MHz",
        "",
    ]
    return chr(10).join(lines) + chr(10)


def _validate_port_consistency(
    verilog_top: str, testbench: str, xdc: str,
) -> list[str]:
    """Return a list of human-readable warnings if port names diverge
    between the three RTL artifacts. Empty list = consistent.

    The check is pragmatic: we extract identifier-shaped tokens from the
    Verilog `module ... ( ... );` port block, then verify each one
    appears in both the testbench (used as a wire/reg) and the XDC
    (referenced via `get_ports`). Surface-only - a missing match could
    legitimately be due to renamed buses, but it always deserves a glance.
    """
    import re as _re
    warnings: list[str] = []
    m = _re.search(r"module\s+\w+\s*\(([^)]+)\);", verilog_top, _re.DOTALL)
    if not m:
        return warnings
    port_block = m.group(1)
    # Pull bare identifiers that look like port names (after direction kw).
    port_names = set()
    for line in port_block.splitlines():
        line = line.split("//", 1)[0].strip().strip(",")
        if not line:
            continue
        toks = _re.findall(r"[a-zA-Z_]\w*", line)
        # Skip leading direction / type words.
        for kw in ("input", "output", "inout", "wire", "reg", "logic"):
            while toks and toks[0] == kw:
                toks.pop(0)
        # The last identifier on the line is the port name (after width).
        if toks:
            port_names.add(toks[-1])

    for p in port_names:
        if testbench and p not in testbench:
            warnings.append(
                f"port `{p}` declared in fpga_top.v but not referenced in testbench"
            )
        if xdc and p not in xdc:
            # Only a subset of ports get pin constraints; flag only when XDC
            # has any get_ports references at all (avoids spurious warnings
            # on a XDC that's pure timing without pin assignments).
            if "get_ports" in xdc:
                warnings.append(
                    f"port `{p}` not referenced in constraints.xdc (get_ports)"
                )
    return warnings


