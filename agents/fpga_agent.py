    def _build_skeleton_vhdl(self, project_name: str, safe_name: str, glr: str) -> Dict[str, str]:
        """Deterministic VHDL skeleton mirror of the Verilog one.

        Emits an entity + architecture for the top, a tb wrapping it, and
        a Vivado XDC. Designed so that switching hdl_language between
        'verilog' and 'vhdl' produces functionally equivalent designs -
        same register addresses, same default values, same testbench
        scenarios.
        """
        module = re.sub(r"[^a-z0-9_]", "_", safe_name.lower()) or "fpga_top"
        glr_l  = (glr or "").lower()
        clk_mhz = 100
        m = re.search(r"(\d+)\s*mhz", glr_l)
        if m:
            clk_mhz = int(m.group(1))
        clk_period = round(1000.0 / clk_mhz, 3)

        has_spi  = "spi"  in glr_l
        has_adc  = "adc"  in glr_l or "lvds" in glr_l
        has_uart = "uart" in glr_l or "serial" in glr_l

        # Register defaults - kept in sync with _build_skeleton_verilog.
        regs = [
            ("BOARD_ID",     "x\"000\"", "RO", "x\"0001\"", "Board identification code"),
            ("BOARD_VERSION","x\"001\"", "RO", "x\"0010\"", "Hardware version"),
            ("CTRL",         "x\"002\"", "RW", "x\"0000\"", "Master control"),
            ("STATUS",       "x\"003\"", "RO", None,          "Status flags"),
            ("VERSION",      "x\"004\"", "RO", "x\"0100\"", "Firmware version"),
            ("SCRATCHPAD",   "x\"005\"", "RW", "x\"0000\"", "R/W test register"),
        ]

        v = []
        v.append(f"-- ============================================================")
        v.append(f"-- Entity   : {module}_top")
        v.append(f"-- Project  : {project_name}")
        v.append(f"-- Clock    : {clk_mhz} MHz ({clk_period} ns period)")
        v.append(f"-- Source   : Silicon to Software (S2S) v2 (auto-generated, VHDL)")
        v.append(f"-- ============================================================")
        v.append("library ieee;")
        v.append("use ieee.std_logic_1164.all;")
        v.append("use ieee.numeric_std.all;")
        v.append("")
        v.append(f"entity {module}_top is")
        v.append("    port (")
        v.append("        clk        : in  std_logic;")
        v.append("        rst_n      : in  std_logic;")
        v.append("        reg_addr   : in  std_logic_vector(15 downto 0);")
        v.append("        reg_wdata  : in  std_logic_vector(15 downto 0);")
        v.append("        reg_rdata  : out std_logic_vector(15 downto 0);")
        v.append("        reg_wr     : in  std_logic;")
        v.append("        reg_rd     : in  std_logic;")
        v.append("        irq_out    : out std_logic;")
        v.append("        busy       : out std_logic;")
        v.append("        error_flag : out std_logic" + (";" if has_spi or has_adc or has_uart else ""))
        if has_spi:
            v.append("        ; spi_clk    : out std_logic")
            v.append("        ; spi_mosi   : out std_logic")
            v.append("        ; spi_miso   : in  std_logic")
            v.append("        ; spi_cs_n   : out std_logic_vector(3 downto 0)")
        if has_adc:
            v.append("        ; adc_data   : in  std_logic_vector(15 downto 0)")
            v.append("        ; adc_data_v : in  std_logic")
            v.append("        ; adc_sync   : out std_logic")
        if has_uart:
            v.append("        ; uart_rxd   : in  std_logic")
            v.append("        ; uart_txd   : out std_logic")
        v.append("    );")
        v.append(f"end entity {module}_top;")
        v.append("")
        v.append(f"architecture rtl of {module}_top is")
        v.append("    -- Register address constants")
        for name, addr, _rw, _reset, _desc in regs:
            v.append(f"    constant ADDR_{name:<14}: std_logic_vector(11 downto 0) := {addr};")
        v.append("")
        v.append("    -- Register storage")
        v.append("    signal ctrl_r       : std_logic_vector(15 downto 0) := (others => '0');")
        v.append("    signal status_r     : std_logic_vector(15 downto 0) := (others => '0');")
        v.append("    signal scratchpad_r : std_logic_vector(15 downto 0) := (others => '0');")
        v.append("    signal busy_r       : std_logic := '0';")
        v.append("    signal irq_r        : std_logic := '0';")
        v.append("begin")
        v.append("    -- Register write")
        v.append("    process (clk)")
        v.append("    begin")
        v.append("        if rising_edge(clk) then")
        v.append("            if rst_n = '0' then")
        v.append("                ctrl_r       <= (others => '0');")
        v.append("                scratchpad_r <= (others => '0');")
        v.append("            elsif reg_wr = '1' then")
        v.append("                case reg_addr(11 downto 0) is")
        v.append("                    when ADDR_CTRL       => ctrl_r       <= reg_wdata;")
        v.append("                    when ADDR_SCRATCHPAD => scratchpad_r <= reg_wdata;")
        v.append("                    when others => null;")
        v.append("                end case;")
        v.append("            end if;")
        v.append("        end if;")
        v.append("    end process;")
        v.append("")
        v.append("    -- Register read")
        v.append("    process (clk)")
        v.append("    begin")
        v.append("        if rising_edge(clk) then")
        v.append("            if rst_n = '0' then")
        v.append("                reg_rdata <= (others => '0');  -- Idle / no read in flight")
        v.append("            elsif reg_rd = '1' then")
        v.append("                case reg_addr(11 downto 0) is")
        v.append("                    when ADDR_BOARD_ID      => reg_rdata <= x\"0001\";")
        v.append("                    when ADDR_BOARD_VERSION => reg_rdata <= x\"0010\";")
        v.append("                    when ADDR_CTRL          => reg_rdata <= ctrl_r;")
        v.append("                    when ADDR_STATUS        => reg_rdata <= status_r;")
        v.append("                    when ADDR_VERSION       => reg_rdata <= x\"0100\";")
        v.append("                    when ADDR_SCRATCHPAD    => reg_rdata <= scratchpad_r;")
        v.append("                    when others             => reg_rdata <= (others => '0');  -- Unmapped: read-as-zero")
        v.append("                end case;")
        v.append("            end if;")
        v.append("        end if;")
        v.append("    end process;")
        v.append("")
        v.append("    -- Status / control wiring")
        v.append("    busy       <= busy_r;")
        v.append("    irq_out    <= irq_r;")
        v.append("    error_flag <= '0';")
        v.append("    status_r   <= x\"00\" & x\"0\" & \"000\" & busy_r;  -- bit0 = busy")
        v.append("end architecture rtl;")
        verilog = chr(10).join(v)

        # Testbench
        tb = []
        tb.append(f"-- Testbench : {module}_top_tb (VHDL)")
        tb.append(f"-- Project   : {project_name}")
        tb.append("library ieee;")
        tb.append("use ieee.std_logic_1164.all;")
        tb.append("use ieee.numeric_std.all;")
        tb.append("")
        tb.append(f"entity {module}_top_tb is end entity;")
        tb.append("")
        tb.append(f"architecture sim of {module}_top_tb is")
        tb.append("    signal clk        : std_logic := '0';")
        tb.append("    signal rst_n      : std_logic := '0';")
        tb.append("    signal reg_addr   : std_logic_vector(15 downto 0) := (others => '0');")
        tb.append("    signal reg_wdata  : std_logic_vector(15 downto 0) := (others => '0');")
        tb.append("    signal reg_rdata  : std_logic_vector(15 downto 0);")
        tb.append("    signal reg_wr     : std_logic := '0';")
        tb.append("    signal reg_rd     : std_logic := '0';")
        tb.append("    signal irq_out    : std_logic;")
        tb.append("    signal busy       : std_logic;")
        tb.append("    signal error_flag : std_logic;")
        tb.append(f"    constant CLK_PERIOD : time := {clk_period} ns;")
        tb.append("begin")
        tb.append(f"    dut: entity work.{module}_top port map (")
        tb.append("        clk=>clk, rst_n=>rst_n,")
        tb.append("        reg_addr=>reg_addr, reg_wdata=>reg_wdata,")
        tb.append("        reg_rdata=>reg_rdata, reg_wr=>reg_wr, reg_rd=>reg_rd,")
        tb.append("        irq_out=>irq_out, busy=>busy, error_flag=>error_flag")
        tb.append("    );")
        tb.append("    clk <= not clk after CLK_PERIOD/2;")
        tb.append("    process")
        tb.append("    begin")
        tb.append("        wait for 5 * CLK_PERIOD;")
        tb.append("        rst_n <= '1';")
        tb.append("        wait for 2 * CLK_PERIOD;")
        tb.append("        -- T1: VERSION register read")
        tb.append("        reg_addr <= x\"0004\"; reg_rd <= '1'; wait for CLK_PERIOD; reg_rd <= '0';")
        tb.append("        wait for CLK_PERIOD;")
        tb.append("        assert reg_rdata = x\"0100\" report \"VERSION read mismatch\" severity error;")
        tb.append("        -- T2: SCRATCHPAD R/W")
        tb.append("        reg_addr <= x\"0005\"; reg_wdata <= x\"CAFE\"; reg_wr <= '1';")
        tb.append("        wait for CLK_PERIOD; reg_wr <= '0'; wait for CLK_PERIOD;")
        tb.append("        reg_rd <= '1'; wait for CLK_PERIOD; reg_rd <= '0'; wait for CLK_PERIOD;")
        tb.append("        assert reg_rdata = x\"CAFE\" report \"SCRATCH read mismatch\" severity error;")
        tb.append("        report \"VHDL skeleton testbench complete\";")
        tb.append("        wait;")
        tb.append("    end process;")
        tb.append("end architecture;")
        testbench = chr(10).join(tb)

        # XDC - identical shape to the Verilog skeleton's
        xdc_lines = [
            f"# Constraints : {project_name} (VHDL)",
            f"# Target      : xc7a35tcpg236-1 (Artix-7)",
            f"create_clock -period {clk_period:.3f} -name clk -waveform {{0 {clk_period/2:.3f}}} [get_ports clk]",
        ]
        xdc = chr(10).join(xdc_lines)

        return {
            "rtl/fpga_top.vhd":       verilog,
            "rtl/fpga_testbench.vhd": testbench,
            "rtl/constraints.xdc":    xdc,
            "fpga_design_report.md":  f"# FPGA Design Report (VHDL skeleton)\n## {project_name}\n\n"
                                      f"Module `{module}_top` emitted as VHDL-2008. Clock {clk_mhz} MHz.\n",
        }

