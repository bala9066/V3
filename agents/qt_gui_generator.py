class SerialWorker(QThread):
    """Non-blocking serial read thread."""
    data_received = Signal(str)
    connection_changed = Signal(bool)
    error_occurred = Signal(str)

    def __init__(self, port: str, baud: int, parent=None):
        super().__init__(parent)
        self.port = port
        self.baud = baud
        self._running = False
        self._serial: Optional[object] = None

    def run(self) -> None:
        if not SERIAL_AVAILABLE:
            self.error_occurred.emit("pyserial not installed. Run: pip install pyserial")
            return
        try:
            self._serial = serial.Serial(self.port, self.baud, timeout=0.1)
            self._running = True
            self.connection_changed.emit(True)
            while self._running:
                if self._serial.in_waiting:
                    raw = self._serial.readline().decode("utf-8", errors="replace").strip()
                    if raw:
                        self.data_received.emit(raw)
                self.msleep(10)
        except Exception as e:
            self.error_occurred.emit(str(e))
        finally:
            if self._serial and self._serial.is_open:
                self._serial.close()
            self.connection_changed.emit(False)

    def send(self, data: bytes) -> None:
        if self._serial and self._serial.is_open:
            self._serial.write(data)

    def stop(self) -> None:
        self._running = False
        self.wait(2000)


