class SettingsTab(QWidget):
    """Serial port configuration."""

    connect_requested    = Signal(str, int)
    disconnect_requested = Signal()

    def __init__(self, parent=None):
        super().__init__(parent)
        self._settings = QSettings("HardwarePipeline", "__PROJECT_NAME__")
        layout = QVBoxLayout(self)

        grp = QGroupBox("Serial Connection")
        form = QGridLayout(grp)

        form.addWidget(QLabel("Port:"), 0, 0)
        self.port_combo = QComboBox()
        self._refresh_ports()
        form.addWidget(self.port_combo, 0, 1)

        refresh_btn = QPushButton("Refresh")
        refresh_btn.clicked.connect(self._refresh_ports)
        form.addWidget(refresh_btn, 0, 2)

        form.addWidget(QLabel("Baud Rate:"), 1, 0)
        self.baud_combo = QComboBox()
        for b in [9600, 19200, 38400, 57600, 115200, 230400, 460800, 921600]:
            self.baud_combo.addItem(str(b), b)
        self.baud_combo.setCurrentText(
            self._settings.value("baud", "115200")
        )
        form.addWidget(self.baud_combo, 1, 1)

        self.connect_btn    = QPushButton("Connect")
        self.disconnect_btn = QPushButton("Disconnect")
        self.disconnect_btn.setEnabled(False)
        form.addWidget(self.connect_btn, 2, 0)
        form.addWidget(self.disconnect_btn, 2, 1)
        layout.addWidget(grp)
        layout.addStretch()

        self.connect_btn.clicked.connect(self._on_connect)
        self.disconnect_btn.clicked.connect(self.disconnect_requested.emit)

    def _refresh_ports(self) -> None:
        self.port_combo.clear()
        if SERIAL_AVAILABLE:
            ports = serial.tools.list_ports.comports()
            for p in ports:
                self.port_combo.addItem(f"{{p.device}} — {{p.description}}", p.device)
        if self.port_combo.count() == 0:
            self.port_combo.addItem("No ports found", "")

    def _on_connect(self) -> None:
        port = self.port_combo.currentData() or self.port_combo.currentText().split(" ")[0]
        baud = int(self.baud_combo.currentData())
        self._settings.setValue("baud", str(baud))
        self.connect_requested.emit(port, baud)

    def set_connected(self, connected: bool) -> None:
        self.connect_btn.setEnabled(not connected)
        self.disconnect_btn.setEnabled(connected)


