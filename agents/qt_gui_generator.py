class MainWindow(QMainWindow):
    """Main application window — __PROJECT_NAME__ Hardware Control GUI."""

    def __init__(self):
        super().__init__()
        self.setWindowTitle("__PROJECT_NAME__ — Hardware Control GUI")
        self.resize(1000, 700)
        self._worker: Optional[SerialWorker] = None
        self._poll_timer = QTimer(self)
        self._poll_timer.setInterval(500)
        self._poll_timer.timeout.connect(self._poll_registers)

        self._setup_ui()
        self._apply_dark_theme()

    def _setup_ui(self) -> None:
        tabs = QTabWidget()
        self.dashboard = DashboardTab()
        self.control   = ControlTab()
        self.log_tab   = QTextEdit()
        self.log_tab.setReadOnly(True)
        self.log_tab.setFont(QFont("Courier New", 10))
        self.settings_tab = SettingsTab()

        tabs.addTab(self.dashboard,    "Dashboard")
        tabs.addTab(self.control,      "Control")
        tabs.addTab(self.log_tab,      "Log")
        tabs.addTab(self.settings_tab, "Settings")
        self.setCentralWidget(tabs)

        self.statusBar().showMessage("Disconnected")

        # Wire signals
        self.settings_tab.connect_requested.connect(self._on_connect)
        self.settings_tab.disconnect_requested.connect(self._on_disconnect)
        self.control.command_requested.connect(self._send_command)

    def _on_connect(self, port: str, baud: int) -> None:
        if self._worker:
            self._on_disconnect()
        self._worker = SerialWorker(port, baud, self)
        self._worker.data_received.connect(self._on_data)
        self._worker.connection_changed.connect(self._on_connection_changed)
        self._worker.error_occurred.connect(self._on_error)
        self._worker.start()
        self._log(f"Connecting to {{port}} @ {{baud}} baud...")

    def _on_disconnect(self) -> None:
        if self._worker:
            self._poll_timer.stop()
            self._worker.stop()
            self._worker = None
        self._on_connection_changed(False)
        self._log("Disconnected.")

    def _on_connection_changed(self, connected: bool) -> None:
        self.dashboard.set_status(connected)
        self.settings_tab.set_connected(connected)
        self.statusBar().showMessage("Connected" if connected else "Disconnected")
        if connected:
            self._poll_timer.start()
        else:
            self._poll_timer.stop()

    def _on_data(self, data: str) -> None:
        self._log(f"RX: {{data}}")
        # Parse simple "REG:0=0x42" format
        if data.startswith("REG:"):
            try:
                parts = data[4:].split("=")
                reg_idx = int(parts[0])
                reg_val = parts[1].strip()
                self.dashboard.update_register(reg_idx, reg_val)
            except (IndexError, ValueError):
                pass

    def _on_error(self, msg: str) -> None:
        self._log(f"ERROR: {{msg}}")
        QMessageBox.critical(self, "Connection Error", msg)
        self._on_connection_changed(False)

    def _send_command(self, data: bytes) -> None:
        if self._worker:
            self._worker.send(data)
            self._log(f"TX: {{data.decode('utf-8', errors='replace').strip()}}")
        else:
            self._log("Not connected — command not sent.")

    def _poll_registers(self) -> None:
        self._send_command(b"CMD:READ_ALL\\n")

    def _log(self, msg: str) -> None:
        ts = datetime.now().strftime("%H:%M:%S.%f")[:-3]
        self.log_tab.append(f"[{{ts}}] {{msg}}")

    def _apply_dark_theme(self) -> None:
        palette = QPalette()
        palette.setColor(QPalette.Window,          QColor("#070b14"))
        palette.setColor(QPalette.WindowText,      QColor("#e2e8f0"))
        palette.setColor(QPalette.Base,            QColor("#1a2235"))
        palette.setColor(QPalette.AlternateBase,   QColor("#2a3a50"))
        palette.setColor(QPalette.ToolTipBase,     QColor("#1a2235"))
        palette.setColor(QPalette.ToolTipText,     QColor("#e2e8f0"))
        palette.setColor(QPalette.Text,            QColor("#e2e8f0"))
        palette.setColor(QPalette.Button,          QColor("#2a3a50"))
        palette.setColor(QPalette.ButtonText,      QColor("#e2e8f0"))
        palette.setColor(QPalette.Highlight,       QColor("#00c6a7"))
        palette.setColor(QPalette.HighlightedText, QColor("#070b14"))
        QApplication.setPalette(palette)

    def closeEvent(self, event) -> None:
        self._on_disconnect()
        event.accept()


