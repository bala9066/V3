class DashboardTab(QWidget):
    """Live device status and register values."""

    REGISTERS = [
        ("CTRL_REG",   "0x00", "RW", "Control register"),
        ("STATUS_REG", "0x01", "RO", "Device status"),
        ("DATA_REG",   "0x02", "RW", "Data register"),
        ("INT_REG",    "0x03", "RW", "Interrupt enable"),
        ("ERR_REG",    "0x04", "RO", "Error flags"),
    ]

    def __init__(self, parent=None):
        super().__init__(parent)
        layout = QVBoxLayout(self)

        # Status cards row
        card_row = QHBoxLayout()
        self.status_card = self._make_card("Device Status", "DISCONNECTED", "#dc2626")
        self.fw_card     = self._make_card("Firmware",      "v1.0.0",       "#3b82f6")
        self.uptime_card = self._make_card("Uptime",        "—",             "#00c6a7")
        card_row.addWidget(self.status_card)
        card_row.addWidget(self.fw_card)
        card_row.addWidget(self.uptime_card)
        layout.addLayout(card_row)

        # Register table
        grp = QGroupBox("Register Map")
        grp_layout = QVBoxLayout(grp)
        self.reg_table = QTableWidget(len(self.REGISTERS), 4)
        self.reg_table.setHorizontalHeaderLabels(["Register", "Address", "Value", "Description"])
        self.reg_table.horizontalHeader().setStretchLastSection(True)
        for row, (name, addr, acc, desc) in enumerate(self.REGISTERS):
            self.reg_table.setItem(row, 0, QTableWidgetItem(name))
            self.reg_table.setItem(row, 1, QTableWidgetItem(addr))
            self.reg_table.setItem(row, 2, QTableWidgetItem("0x--"))
            self.reg_table.setItem(row, 3, QTableWidgetItem(desc))
        grp_layout.addWidget(self.reg_table)
        layout.addWidget(grp)

    def _make_card(self, title: str, value: str, color: str) -> QGroupBox:
        card = QGroupBox(title)
        v = QVBoxLayout(card)
        lbl = QLabel(value)
        lbl.setAlignment(Qt.AlignCenter)
        lbl.setStyleSheet(f"font-size: 18px; font-weight: bold; color: {color};")
        lbl.setObjectName(f"card_{title.replace(' ','_')}")
        v.addWidget(lbl)
        return card

    def update_register(self, row: int, value: str) -> None:
        if 0 <= row < self.reg_table.rowCount():
            self.reg_table.setItem(row, 2, QTableWidgetItem(value))

    def set_status(self, connected: bool) -> None:
        lbl = self.status_card.findChild(QLabel, "card_Device_Status")
        if lbl:
            if connected:
                lbl.setText("CONNECTED")
                lbl.setStyleSheet("font-size:18px;font-weight:bold;color:#00c6a7;")
            else:
                lbl.setText("DISCONNECTED")
                lbl.setStyleSheet("font-size:18px;font-weight:bold;color:#dc2626;")


