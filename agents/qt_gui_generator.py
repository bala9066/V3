"""
PySide6 Qt GUI Skeleton Generator.

Called from P8c (CodeAgent) after device drivers are generated.
Uses LLM to produce a runnable PySide6 application that:
  - Connects to the hardware over serial / USB
  - Displays real-time register values
  - Provides control panels matching SDD interfaces
  - Follows the design from SDD section 2.5 (Interface Viewpoint)
"""

import logging

from config import settings

logger = logging.getLogger(__name__)

QT_SYSTEM_PROMPT = """You are an expert PySide6/Qt6 developer generating a complete, runnable GUI application for embedded hardware control.

## OUTPUT REQUIREMENTS:
Generate a single Python file named `gui_application.py` that:

1. Uses PySide6 (NOT PyQt5/PyQt6/tkinter)
2. Has a QMainWindow with:
   - MenuBar (File, View, Help)
   - StatusBar showing connection state
   - Central widget with QTabWidget

3. Tabs:
   - "Dashboard" — live device status, register values in QTableWidget
   - "Control" — buttons and sliders matching the hardware interfaces from SDD
   - "Log" — QTextEdit showing serial/communication log
   - "Settings" — port selector (QComboBox), baud rate, connect/disconnect

4. Serial/USB Communication:
   - Use QThread for non-blocking serial reads (pyserial)
   - QTimer for periodic register polling (500ms)
   - Signals/slots for thread-safe GUI updates

5. Code standards:
   - Type hints throughout
   - Docstrings on all classes/methods
   - Error handling with QMessageBox for connection failures
   - QSettings for saving last-used port/baud
   - Dark theme using QPalette or qdarkstyle

## IMPORTANT:
- Generate COMPLETE runnable code — no placeholders or "TODO" stubs
- The register names and hardware interfaces MUST match what is described in the SDD
- Include `if __name__ == "__main__":` block
- The file must run standalone: `python gui_application.py`
"""


async def generate_qt_gui(
    project_name: str,
    sdd_content: str,
    srs_content: str,
    call_llm_fn,
) -> str:
    """
    Generate a PySide6 GUI application from SDD/SRS context.

    Args:
        project_name: Human-readable project name
        sdd_content:  Content of the Software Design Document
        srs_content:  Content of the Software Requirements Specification
        call_llm_fn:  Coroutine: async (messages, system, model) -> {content: str}

    Returns:
        Python source code string for the GUI application
    """

    prompt = f"""Generate a complete PySide6 Qt GUI application for the following hardware project.

**Project Name:** {project_name}

## Software Design Document (SDD) — Interface & Architecture
{sdd_content[:4000] if sdd_content else "No SDD available — use generic hardware control layout."}

## Software Requirements Specification (SRS) — Functional Requirements
{srs_content[:2000] if srs_content else "No SRS available."}

---

Generate `gui_application.py`:
- Use the exact register names, interface names, and module names from the SDD above
- The Dashboard tab must show the registers defined in the SDD Interface Viewpoint
- The Control tab buttons must match the driver API functions listed in the SDD
- Include serial port selection for the hardware described in the SRS

Output ONLY the Python code — no explanations, no markdown fences, just the file content.
Start with: # gui_application.py — {project_name} Hardware Control GUI
"""

    try:
        response = await call_llm_fn(
            messages=[{"role": "user", "content": prompt}],
            system=QT_SYSTEM_PROMPT,
            model=settings.primary_model,
        )
        code = response.get("content", "")

        # Strip markdown fences if LLM wrapped in ```python
        if code.startswith("```"):
            import re
            code = re.sub(r"^```(?:python)?\n?", "", code)
            code = re.sub(r"\n?```$", "", code.rstrip())

        if not code or len(code) < 200:
            logger.warning("Qt GUI generation returned empty/short response — using fallback template")
            return _fallback_gui_template(project_name)

        return code

    except Exception as e:
        logger.error(f"Qt GUI generation failed: {e}")
        return _fallback_gui_template(project_name)


