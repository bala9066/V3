def spi_master() -> str:
    return _BANNER + """
// spi_master.v - generic 8/16/24-bit SPI master, mode 0 default.
// Strobes `start` to begin a transfer; `done` pulses when MISO data
// has been latched into `rx_data`.

module spi_master #(
    parameter integer DATA_W   = 24,
    parameter integer CLK_DIV  = 16   // sclk = clk / (2*CLK_DIV)
) (
    input  wire                 clk,
    input  wire                 rst_n,
    input  wire                 start,
    input  wire [DATA_W-1:0]    tx_data,
    output reg  [DATA_W-1:0]    rx_data,
    output reg                  done,
    // Physical pins
    output reg                  sclk,
    output reg                  mosi,
    input  wire                 miso,
    output reg                  cs_n
);
    reg [15:0] div_cnt;
    reg [DATA_W-1:0] sh_tx, sh_rx;
    reg [5:0]  bit_cnt;
    reg        busy;

    always @(posedge clk) begin
        done <= 1'b0;
        if (!rst_n) begin
            sclk <= 1'b0; cs_n <= 1'b1; busy <= 1'b0;
            div_cnt <= 0; bit_cnt <= 0; mosi <= 1'b0;
        end else if (!busy && start) begin
            sh_tx <= tx_data; sh_rx <= 0;
            cs_n  <= 1'b0; sclk <= 1'b0;
            bit_cnt <= DATA_W;
            div_cnt <= 0; busy <= 1'b1;
            mosi <= tx_data[DATA_W-1];
        end else if (busy) begin
            if (div_cnt == CLK_DIV-1) begin
                div_cnt <= 0;
                sclk <= ~sclk;
                if (sclk == 1'b0) begin
                    // rising edge - sample MISO
                    sh_rx <= {sh_rx[DATA_W-2:0], miso};
                end else begin
                    // falling edge - drive next MOSI bit
                    sh_tx <= {sh_tx[DATA_W-2:0], 1'b0};
                    mosi  <= sh_tx[DATA_W-2];
                    bit_cnt <= bit_cnt - 1;
                    if (bit_cnt == 1) begin
                        cs_n <= 1'b1; busy <= 1'b0;
                        rx_data <= {sh_rx[DATA_W-2:0], miso};
                        done <= 1'b1;
                    end
                end
            end else div_cnt <= div_cnt + 1;
        end
    end
endmodule
"""


def pll_config_sequencer(spec=None) -> str:
    """Generate PLL config sequencer parameterised by ComponentSpec.

    Spec.pll_config_reg_count tells the sequencer how many registers
    the chip needs at boot (HMC830 has ~12, LMK04832 has ~160). Without
    the spec we emit a simple 2-register N/R sequencer.
    """
    n_regs = (spec.pll_config_reg_count if spec else None) or 2
    bits   = (spec.pll_config_bit_width if spec else None) or 24
    mpn    = spec.mpn if spec else "PLL (generic)"
    src_tag = spec.source if spec else "default"
    conf    = spec.confidence if spec else 0.0
    _hdr = (
        f"// pll_config.v - sequencer for {mpn} "
        f"({n_regs} config registers, {bits}-bit SPI words).\n"
        f"// Spec source: {src_tag} (confidence {conf})\n"
        f"//\n"
        f"// Watches PLL_CTRL[0] (LATCH) writes and serialises\n"
        f"// PLL_N_DIV, PLL_R_DIV out to the PLL chip via spi_master.\n"
    )
    return _BANNER + _hdr + r"""

module pll_config (
    input  wire        clk,
    input  wire        rst_n,
    input  wire [15:0] pll_ctrl,
    input  wire [15:0] pll_n_div,
    input  wire [15:0] pll_r_div,
    output reg         pll_locked,
    // SPI master ports
    output reg         spi_start,
    output reg  [23:0] spi_tx_data,
    input  wire [23:0] spi_rx_data,
    input  wire        spi_done
);
    localparam S_IDLE = 2'd0, S_TX_N = 2'd1, S_TX_R = 2'd2, S_DONE = 2'd3;
    reg [1:0] state = S_IDLE;
    reg pll_ctrl_d = 1'b0;

    always @(posedge clk) begin
        spi_start <= 1'b0;
        pll_ctrl_d <= pll_ctrl[0];
        if (!rst_n) begin
            state <= S_IDLE; pll_locked <= 1'b0;
        end else case (state)
            S_IDLE: if (pll_ctrl[0] && !pll_ctrl_d) begin
                spi_tx_data <= {8'h01, pll_n_div};   // reg 0x01 = N divider
                spi_start <= 1'b1; state <= S_TX_N;
            end
            S_TX_N: if (spi_done) begin
                spi_tx_data <= {8'h02, pll_r_div};   // reg 0x02 = R divider
                spi_start <= 1'b1; state <= S_TX_R;
            end
            S_TX_R: if (spi_done) begin
                pll_locked <= 1'b1; state <= S_DONE;
            end
            S_DONE: if (!pll_ctrl[0]) state <= S_IDLE;
        endcase
    end
endmodule
"""


