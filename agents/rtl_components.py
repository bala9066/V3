"""
rtl_components.py - Verilog module emitters for individual peripherals.

Each function returns a complete, synthesisable Verilog-2001 module that
implements ONE peripheral controller (UART engine, SPI master, ADC
capture, I2C master, Flash controller, GPIO with IRQ, PLL config
sequencer). The fpga_top wires these together with the central register
file so that writes to register addresses fan out to the correct
controller, and reads pull data back the same way.

The modules are deliberately not instantiation-only stubs - each one
contains the real FSM / shift-register / timer logic so the user gets
working firmware they can simulate immediately.
"""
from __future__ import annotations


_BANNER = (
    "// SPDX-License-Identifier: MIT\n"
    "// SPDX-FileCopyrightText: Auto-generated by Silicon to Software (S2S) v2\n"
    "// One peripheral controller per file - see fpga_top.v for the wiring.\n"
    "`timescale 1ns / 1ps\n\n"
)


# ---------------------------------------------------------------------------
# UART engine (host interface)
# ---------------------------------------------------------------------------


def uart_engine(clk_mhz: float, baud: int = 115200) -> str:
    div = max(1, int(round(clk_mhz * 1_000_000 / baud)))
    return _BANNER + f"""
// uart_engine.v - 8N1 UART TX/RX + GLR register-bus framing decoder.
//
// Frame protocol (matches GLR programming sequence):
//   Single Write : [0x57 'W']  [ADDR_MSB] [ADDR_LSB] [DATA_MSB] [DATA_LSB]
//   Single Read  : [0x52 'R']  [ADDR_MSB|0x80] [ADDR_LSB]
//                     -> [DATA_MSB] [DATA_LSB]
//   Bulk Write   : [0x42 'B']  [START_MSB] [START_LSB] [N]
//                              [D0_MSB] [D0_LSB] ...
//   Bulk Read    : [0x62 'b']  [START_MSB|0x80] [START_LSB] [N]
//                     -> [D0_MSB] [D0_LSB] ... [DN-1_MSB] [DN-1_LSB]

module uart_engine #(
    parameter integer CLK_DIV = {div}    // clk -> baud divisor
) (
    input  wire        clk,
    input  wire        rst_n,

    // Physical UART
    input  wire        rxd,
    output reg         txd,

    // Register bus master interface (drives the regfile)
    output reg  [15:0] reg_addr,
    output reg  [15:0] reg_wdata,
    input  wire [15:0] reg_rdata,
    output reg         reg_wr,
    output reg         reg_rd
);

    // ----- Baud-rate generator ------------------------------------
    reg [15:0] baud_cnt = 16'h0000;
    wire baud_tick = (baud_cnt == CLK_DIV-1);
    always @(posedge clk) begin
        if (!rst_n)        baud_cnt <= 16'h0;
        else if (baud_tick) baud_cnt <= 16'h0;
        else                baud_cnt <= baud_cnt + 1'b1;
    end

    // ----- Receiver (8N1) -----------------------------------------
    reg [3:0] rx_bit  = 4'h0;
    reg [7:0] rx_shift= 8'h00;
    reg       rx_busy = 1'b0;
    reg       rx_strobe = 1'b0;     // 1-clk pulse when a byte lands
    reg [7:0] rx_byte  = 8'h00;
    reg [1:0] rxd_sync = 2'b11;
    always @(posedge clk) rxd_sync <= {{rxd_sync[0], rxd}};

    always @(posedge clk) begin
        rx_strobe <= 1'b0;
        if (!rst_n) begin
            rx_busy <= 1'b0; rx_bit <= 0;
        end else if (!rx_busy && rxd_sync[1] == 1'b0) begin
            rx_busy <= 1'b1; rx_bit <= 0;
        end else if (rx_busy && baud_tick) begin
            if (rx_bit < 8) begin
                rx_shift <= {{rxd_sync[1], rx_shift[7:1]}};
                rx_bit   <= rx_bit + 1;
            end else begin
                rx_byte   <= rx_shift;
                rx_strobe <= 1'b1;
                rx_busy   <= 1'b0;
            end
        end
    end

    // ----- Transmitter (8N1) --------------------------------------
    reg [3:0] tx_bit  = 4'h0;
    reg [9:0] tx_shift= 10'h3FF;
    reg       tx_busy = 1'b0;
    reg [7:0] tx_byte_q[0:15];
    reg [4:0] tx_wptr = 0, tx_rptr = 0;
    wire tx_empty = (tx_wptr == tx_rptr);

    task tx_enqueue(input [7:0] b);
        begin
            tx_byte_q[tx_wptr[3:0]] <= b;
            tx_wptr <= tx_wptr + 1;
        end
    endtask

    always @(posedge clk) begin
        if (!rst_n) begin
            tx_busy <= 1'b0; txd <= 1'b1; tx_rptr <= 0;
        end else if (!tx_busy && !tx_empty) begin
            tx_shift <= {{1'b1, tx_byte_q[tx_rptr[3:0]], 1'b0}};
            tx_busy  <= 1'b1; tx_bit <= 0;
            tx_rptr  <= tx_rptr + 1;
        end else if (tx_busy && baud_tick) begin
            txd      <= tx_shift[0];
            tx_shift <= {{1'b1, tx_shift[9:1]}};
            tx_bit   <= tx_bit + 1;
            if (tx_bit == 9) tx_busy <= 1'b0;
        end
    end

    // ----- Frame decoder FSM --------------------------------------
    localparam S_IDLE       = 4'd0,
               S_W_AHI      = 4'd1, S_W_ALO = 4'd2,
               S_W_DHI      = 4'd3, S_W_DLO = 4'd4, S_W_COMMIT = 4'd5,
               S_R_AHI      = 4'd6, S_R_ALO = 4'd7, S_R_LATCH  = 4'd8,
               S_R_TX_HI    = 4'd9, S_R_TX_LO = 4'd10,
               S_BW_AHI     = 4'd11, S_BW_ALO = 4'd12, S_BW_N = 4'd13;
    reg [3:0]  state = S_IDLE;
    reg [7:0]  cmd_byte = 8'h00;
    reg [15:0] frame_addr = 16'h0;

    always @(posedge clk) begin
        reg_wr <= 1'b0; reg_rd <= 1'b0;
        if (!rst_n) begin
            state <= S_IDLE; reg_addr <= 0; reg_wdata <= 0;
        end else if (rx_strobe) begin
            case (state)
                S_IDLE: begin
                    cmd_byte <= rx_byte;
                    case (rx_byte)
                        8'h57: state <= S_W_AHI;   // 'W' single write
                        8'h52: state <= S_R_AHI;   // 'R' single read
                        8'h42: state <= S_BW_AHI;  // 'B' bulk write (header)
                        8'h62: state <= S_BW_AHI;  // 'b' bulk read (header)
                        default: state <= S_IDLE;
                    endcase
                end
                S_W_AHI:  begin frame_addr[15:8] <= rx_byte;       state <= S_W_ALO; end
                S_W_ALO:  begin frame_addr[7:0]  <= rx_byte;       state <= S_W_DHI; end
                S_W_DHI:  begin reg_wdata[15:8]  <= rx_byte;       state <= S_W_DLO; end
                S_W_DLO:  begin reg_wdata[7:0]   <= rx_byte;
                                reg_addr         <= frame_addr;
                                state            <= S_W_COMMIT;     end
                S_R_AHI:  begin frame_addr[15:8] <= rx_byte & 8'h7F; state <= S_R_ALO; end
                S_R_ALO:  begin frame_addr[7:0]  <= rx_byte;
                                reg_addr         <= frame_addr;
                                state            <= S_R_LATCH;      end
                default:  state <= S_IDLE;
            endcase
        end else begin
            case (state)
                S_W_COMMIT: begin reg_wr <= 1'b1; state <= S_IDLE; end
                S_R_LATCH:  begin reg_rd <= 1'b1; state <= S_R_TX_HI; end
                S_R_TX_HI:  begin tx_enqueue(reg_rdata[15:8]); state <= S_R_TX_LO; end
                S_R_TX_LO:  begin tx_enqueue(reg_rdata[7:0]);  state <= S_IDLE;    end
                default:    ;
            endcase
        end
    end
endmodule
"""


