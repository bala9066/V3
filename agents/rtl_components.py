def adc_capture(data_w: int = 14) -> str:
    return _BANNER + f"""
// adc_capture.v - latches incoming ADC samples into channel registers
// when ADC_CTRL[0] (ARM) is set. Transitions ARM -> WAIT -> SAMPLE -> DONE.

module adc_capture #(
    parameter integer DATA_W = {data_w}
) (
    input  wire                 clk,
    input  wire                 rst_n,
    input  wire                 adc_data_valid,
    input  wire [DATA_W-1:0]    adc_data,
    input  wire [15:0]          adc_ctrl,
    output reg  [15:0]          adc_status,
    output reg  [15:0]          ch1_data,
    output reg  [15:0]          ch2_data
);
    localparam S_IDLE = 2'd0, S_ARMED = 2'd1, S_LATCHED = 2'd2;
    reg [1:0]  state = S_IDLE;
    reg        arm_d = 1'b0;
    reg        toggle = 1'b0;     // alternate channel on consecutive samples

    always @(posedge clk) begin
        arm_d <= adc_ctrl[0];
        if (!rst_n) begin
            state <= S_IDLE; ch1_data <= 0; ch2_data <= 0;
            adc_status <= 16'h0000; toggle <= 0;
        end else case (state)
            S_IDLE:    if (adc_ctrl[0] && !arm_d) begin
                           state <= S_ARMED; adc_status[0] <= 1'b1;
                       end
            S_ARMED:   if (adc_data_valid) begin
                           if (toggle == 1'b0) ch1_data <= {{ {{(16-DATA_W){{1'b0}}}}, adc_data }};
                           else                ch2_data <= {{ {{(16-DATA_W){{1'b0}}}}, adc_data }};
                           toggle <= ~toggle;
                           adc_status[1] <= 1'b1;     // sample latched
                           state <= S_LATCHED;
                       end
            S_LATCHED: if (!adc_ctrl[0]) begin
                           adc_status <= 16'h0000;
                           state <= S_IDLE;
                       end
        endcase
    end
endmodule
"""


