// Sheet Index sidebar — shows all sheets in a compact left panel

function SheetIndex({ sheets, activeIdx, onSelect, color }: {
  sheets: SheetData[]; activeIdx: number;
  onSelect: (i: number) => void; color: string;
}) {
  // Count rules-engine components (bypass caps, pull-ups, ESD diodes)
  const rulesCount = (s: SheetData) =>
    s.components.filter(c =>
      c.ref.startsWith('C_BYP') || c.ref.startsWith('R_PU') || c.ref.startsWith('D_ESD')
    ).length;

  return (
    <aside style={{
      width: 190, borderRight: '1px solid var(--border2)',
      background: 'var(--panel)', overflowY: 'auto',
      padding: '10px 0',
      flexShrink: 0,
    }}>
      <div style={{
        fontSize: 9, color: 'var(--text4)', fontFamily: "'DM Mono', monospace",
        letterSpacing: 0.8, padding: '2px 12px 8px', borderBottom: '1px solid var(--border2)',
      }}>
        SHEETS ({sheets.length})
      </div>
      {sheets.map((s, i) => {
        const active = i === activeIdx;
        const rc = rulesCount(s);
        return (
          <button
            key={s.id}
            onClick={() => onSelect(i)}
            style={{
              display: 'block', width: '100%', textAlign: 'left',
              padding: '8px 12px', border: 'none',
              borderLeft: `3px solid ${active ? color : 'transparent'}`,
              background: active ? `${color}12` : 'transparent',
              cursor: 'pointer', transition: 'all 0.12s',
            }}
          >
            <div style={{
              fontSize: 11, fontFamily: "'DM Mono', monospace", fontWeight: active ? 600 : 400,
              color: active ? color : 'var(--text2)', marginBottom: 2,
              whiteSpace: 'nowrap', overflow: 'hidden', textOverflow: 'ellipsis',
            }}>
              {s.title || s.id}
            </div>
            <div style={{
              fontSize: 9, color: 'var(--text4)', fontFamily: "'JetBrains Mono', monospace",
            }}>
              {s.components.length} sym · {s.nets.length} nets
              {rc > 0 && (
                <span style={{ color: '#00c6a7', marginLeft: 4 }}>+{rc} auto</span>
              )}
            </div>
          </button>
        );
      })}
    </aside>
  );
}

