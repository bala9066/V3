// Top-level schematic viewer used as the "Schematic" sub-tab under P4 Documents.
// Handles:
//   • fetching schematic.json from the project output directory
//   • multi-sheet navigation
//   • pan/zoom (native SVG viewBox + wheel / drag)
//   • PDF export (jsPDF + svg2pdf.js, vector output)
//   • hovered-net highlight + net side panel

import { useEffect, useRef, useState, useMemo } from 'react';
import { jsPDF } from 'jspdf';
import { svg2pdf } from 'svg2pdf.js';
import { api } from '../../api';
import SheetSchematic from './SheetSchematic';
import type { SchematicData, SheetData, NetData, ComponentData } from './symbols/types';
import { NET_COLORS } from './symbols/types';

interface Props {
  projectId: number;
  color: string;  // phase color for buttons
}

export default function SchematicView({ projectId, color }: Props) {
  const [data, setData] = useState<SchematicData | null>(null);
  const [loading, setLoading] = useState(true);
  const [err, setErr] = useState<string | null>(null);
  const [sheetIdx, setSheetIdx] = useState(0);
  const [hoveredNet, setHoveredNet] = useState<string | null>(null);
  const [downloading, setDownloading] = useState(false);
  const [dlError, setDlError] = useState<string | null>(null);
  const svgRef = useRef<SVGSVGElement | null>(null);

  // Fetch schematic.json from the output dir — falls back nicely if missing
  useEffect(() => {
    let aborted = false;
    (async () => {
      try {
        setLoading(true);
        setErr(null);
        const raw = await api.getDocumentText(projectId, 'schematic.json');
        if (aborted) return;
        const parsed = JSON.parse(raw) as SchematicData;
        if (!parsed.sheets || parsed.sheets.length === 0) {
          setErr('Schematic JSON has no sheets.');
          return;
        }
        setData(parsed);
      } catch (e: unknown) {
        if (!aborted) setErr(e instanceof Error ? e.message : String(e));
      } finally {
        if (!aborted) setLoading(false);
      }
    })();
    return () => { aborted = true; };
  }, [projectId]);

  const sheet: SheetData | null = useMemo(
    () => (data && data.sheets[sheetIdx]) || null,
    [data, sheetIdx],
  );

  const downloadPdf = async () => {
    if (!svgRef.current || !sheet) return;
    setDownloading(true);
    setDlError(null);
    try {
      // Measure the rendered SVG's viewBox so the PDF page size matches
      const vb = svgRef.current.viewBox.baseVal;
      const wPx = vb.width || svgRef.current.clientWidth || 1200;
      const hPx = vb.height || svgRef.current.clientHeight || 800;
      // Convert px → mm at 96 DPI ≈ 0.264583 mm/px
      const mm = (px: number) => Math.round(px * 0.2646);
      const pdf = new jsPDF({
        orientation: wPx >= hPx ? 'landscape' : 'portrait',
        unit: 'mm',
        format: [mm(wPx) + 20, mm(hPx) + 30],
      });

      // Title + footer before the SVG
      pdf.setFontSize(14);
      pdf.setTextColor(0, 0, 0);
      pdf.text(sheet.title, 10, 12);
      pdf.setFontSize(9);
      pdf.setTextColor(100, 100, 100);
      pdf.text(
        `Silicon to Software (S2S) — Schematic · ${sheet.components.length} components · ${sheet.nets.length} nets`,
        10, 18
      );

      await svg2pdf(svgRef.current, pdf, {
        x: 10, y: 22,
        width: mm(wPx), height: mm(hPx),
      });

      pdf.setFontSize(8);
      pdf.setTextColor(120, 120, 120);
      pdf.text('Generated by Silicon to Software (S2S) · DATA PATTERNS · CODE KNIGHTS', 10, mm(hPx) + 28);

      const safeTitle = (sheet.title || 'schematic').replace(/[^\w\-]+/g, '_').slice(0, 40);
      pdf.save(`${safeTitle}_${sheet.id}.pdf`);
    } catch (e: unknown) {
      setDlError(e instanceof Error ? e.message : String(e));
    } finally {
      setDownloading(false);
    }
  };

  // Loading / error states
  if (loading) {
    return (
      <div style={{ padding: 40, textAlign: 'center', color: 'var(--text3)' }}>
        <div style={{ width: 24, height: 24, margin: '0 auto 12px', borderRadius: '50%',
                      border: `3px solid ${color}`, borderTopColor: 'transparent',
                      animation: 'spin 0.8s linear infinite' }} />
        Loading schematic…
      </div>
    );
  }
  if (err) {
    return (
      <div style={{ padding: 40, textAlign: 'center', color: 'var(--text3)' }}>
        <div style={{ color: '#dc2626', fontSize: 13, marginBottom: 10 }}>Schematic unavailable</div>
        <div style={{ fontSize: 12, color: 'var(--text4)' }}>{err}</div>
        <div style={{ fontSize: 11, color: 'var(--text4)', marginTop: 16 }}>
          Run Phase 4 (Netlist Generation) first — the schematic is emitted alongside the netlist.
        </div>
      </div>
    );
  }
  if (!data || !sheet) return null;

  return (
    <div style={{ display: 'flex', flexDirection: 'column', height: '100%', minHeight: 600 }}>
      {/* Toolbar: sheet tabs + download button */}
      <div style={{
        display: 'flex', alignItems: 'center', justifyContent: 'space-between',
        padding: '10px 14px', borderBottom: '1px solid var(--border2)',
        background: 'var(--panel2)', gap: 10,
      }}>
        {/* Sheet tabs */}
        <div style={{ display: 'flex', gap: 6, overflowX: 'auto' }}>
          {data.sheets.map((s, i) => (
            <button key={s.id} onClick={() => setSheetIdx(i)}
                    style={{
                      padding: '6px 12px',
                      fontSize: 11,
                      fontFamily: "'DM Mono', monospace",
                      letterSpacing: 0.5,
                      border: `1px solid ${i === sheetIdx ? color + '80' : 'var(--panel3)'}`,
                      background: i === sheetIdx ? `${color}20` : 'var(--panel)',
                      color: i === sheetIdx ? color : 'var(--text3)',
                      borderRadius: 5,
                      cursor: 'pointer',
                      whiteSpace: 'nowrap',
                      transition: 'all 0.15s',
                    }}>
              {s.title || s.id}
            </button>
          ))}
          {data.auto_synthesized && (
            <span style={{
              fontSize: 10, color: '#f59e0b',
              background: 'rgba(245,158,11,0.1)',
              border: '1px solid rgba(245,158,11,0.3)',
              borderRadius: 4,
              padding: '3px 8px',
              fontFamily: "'DM Mono', monospace",
              letterSpacing: 0.5,
              alignSelf: 'center',
            }}>AUTO-SYNTHESIZED</span>
          )}
        </div>

        {/* Right side: stats + PDF download */}
        <div style={{ display: 'flex', gap: 10, alignItems: 'center' }}>
          <span style={{ fontSize: 10, color: 'var(--text4)', fontFamily: "'DM Mono', monospace" }}>
            {sheet.components.length} SYMBOLS · {sheet.nets.length} NETS
          </span>
          <button
            onClick={downloadPdf}
            disabled={downloading}
            style={{
              fontSize: 11,
              fontFamily: "'DM Mono', monospace",
              padding: '6px 14px',
              background: downloading ? 'rgba(245,158,11,0.2)' : 'rgba(245,158,11,0.1)',
              color: '#f59e0b',
              border: '1px solid rgba(245,158,11,0.4)',
              borderRadius: 5,
              cursor: downloading ? 'wait' : 'pointer',
              whiteSpace: 'nowrap',
              display: 'flex', alignItems: 'center', gap: 6,
              transition: 'all 0.15s',
            }}>
            {downloading ? (
              <><span style={{ width: 10, height: 10, borderRadius: '50%', border: '2px solid #f59e0b',
                               borderTopColor: 'transparent', animation: 'spin 0.7s linear infinite' }} />
                Exporting PDF…</>
            ) : '↓ Download PDF'}
          </button>
        </div>
      </div>
      {dlError && (
        <div style={{ padding: '6px 14px', background: 'rgba(220,38,38,0.08)',
                      color: '#dc2626', fontSize: 11, fontFamily: "'DM Mono', monospace" }}>
          ⚠ PDF export failed: {dlError}
        </div>
      )}

      {/* Main drawing area + net sidebar */}
      <div style={{ display: 'flex', flex: 1, minHeight: 0 }}>
        <div style={{ flex: 1, overflow: 'auto', background: '#070b14', minHeight: 600 }}>
          <div style={{ minWidth: '100%', minHeight: '100%' }}>
            <SheetSchematic
              ref={svgRef}
              sheet={sheet}
              hoveredNet={hoveredNet}
              onNetHover={setHoveredNet}
            />
          </div>
        </div>
        <NetSidebar sheet={sheet} hoveredNet={hoveredNet} onHover={setHoveredNet} />
      </div>
    </div>
  );
}

