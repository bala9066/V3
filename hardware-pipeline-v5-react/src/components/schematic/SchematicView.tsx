function NetSidebar({ sheet, hoveredNet, onHover }: {
  sheet: SheetData; hoveredNet: string | null; onHover: (n: string | null) => void;
}) {
  // Group nets by type, sort by name within group
  const grouped = useMemo(() => {
    const g: Record<string, NetData[]> = {};
    for (const n of sheet.nets) (g[n.type || 'signal'] ||= []).push(n);
    for (const k of Object.keys(g)) g[k].sort((a, b) => a.name.localeCompare(b.name));
    return g;
  }, [sheet.nets]);

  // Orphan pins — every pin on every component that doesn't appear in any net's endpoints
  const orphanPins = useMemo(() => {
    const connected = new Set<string>();
    for (const net of sheet.nets) for (const ep of net.endpoints) connected.add(`${ep.ref}.${ep.pin}`);
    const orphans: string[] = [];
    for (const c of sheet.components) {
      const pins = expectedPins(c);
      for (const p of pins) {
        if (!connected.has(`${c.ref}.${p}`)) orphans.push(`${c.ref}.${p}`);
      }
    }
    return orphans;
  }, [sheet.components, sheet.nets]);

  return (
    <aside style={{
      width: 280, borderLeft: '1px solid var(--border2)',
      background: 'var(--panel)', overflowY: 'auto',
      padding: '12px 14px',
    }}>
      <div style={{ fontSize: 10, color: 'var(--text4)', fontFamily: "'DM Mono', monospace",
                    letterSpacing: 0.8, marginBottom: 10 }}>
        NETS ({sheet.nets.length})
      </div>
      {Object.entries(grouped).map(([type, list]) => (
        <div key={type} style={{ marginBottom: 14 }}>
          <div style={{
            fontSize: 9, color: NET_COLORS[type] || NET_COLORS.default,
            fontFamily: "'DM Mono', monospace", letterSpacing: 0.6,
            textTransform: 'uppercase', marginBottom: 4,
          }}>
            {type} · {list.length}
          </div>
          {list.map(n => (
            <div
              key={n.name}
              onMouseEnter={() => onHover(n.name)}
              onMouseLeave={() => onHover(null)}
              style={{
                fontSize: 11, fontFamily: "'JetBrains Mono', monospace",
                padding: '4px 8px', marginLeft: 4,
                borderRadius: 3, cursor: 'pointer',
                background: hoveredNet === n.name ? `${NET_COLORS[type] || NET_COLORS.default}22` : 'transparent',
                color: hoveredNet === n.name ? '#e2e8f0' : 'var(--text3)',
                borderLeft: `2px solid ${hoveredNet === n.name ? (NET_COLORS[type] || NET_COLORS.default) : 'transparent'}`,
                transition: 'all 0.12s',
              }}>
              {n.name} <span style={{ color: 'var(--text4)' }}>· {n.endpoints.length} pins</span>
            </div>
          ))}
        </div>
      ))}

      {orphanPins.length > 0 && (
        <div style={{ marginTop: 20, padding: 10, borderRadius: 4,
                      background: 'rgba(220,38,38,0.08)', border: '1px solid rgba(220,38,38,0.25)' }}>
          <div style={{ fontSize: 10, color: '#dc2626', fontFamily: "'DM Mono', monospace",
                        letterSpacing: 0.6, marginBottom: 5 }}>
            ⚠ FLOATING PINS ({orphanPins.length})
          </div>
          <div style={{ fontSize: 10, color: 'var(--text4)', lineHeight: 1.4,
                        fontFamily: "'JetBrains Mono', monospace", maxHeight: 100, overflowY: 'auto' }}>
            {orphanPins.slice(0, 20).join(', ')}
            {orphanPins.length > 20 && ` … +${orphanPins.length - 20} more`}
          </div>
        </div>
      )}
    </aside>
  );
}

function expectedPins(c: ComponentData): string[] {
  if (c.type === 'ic' && c.pins) return c.pins.map(p => p.name);
  if (c.type === 'connector') {
    const n = parseInt((c.value || 'CON_2').replace(/\D+/g, ''), 10) || 2;
    return Array.from({ length: n }, (_, i) => String(i + 1));
  }
  if (['resistor', 'capacitor', 'capacitor_polar', 'inductor',
       'diode', 'diode_zener', 'diode_tvs', 'diode_led'].includes(c.type)) {
    return ['1', '2'];
  }
  if (c.type === 'ground' || c.type === 'vcc') return ['1'];
  return [];
}
