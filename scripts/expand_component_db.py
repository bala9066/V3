def main() -> int:
    total_before = 0
    total_after = 0
    for domain, additions in ADDITIONS.items():
        path = REPO / "domains" / domain / "components.json"
        data = json.loads(path.read_text())
        existing_pns = {c.get("part_number") for c in data.get("components", [])}
        total_before += len(existing_pns)

        added = 0
        for extra in additions:
            if extra["part_number"] in existing_pns:
                continue
            entry = {
                "part_number": extra["part_number"],
                "manufacturer": extra["manufacturer"],
                "category": extra["category"],
                "description": extra.get("description", ""),
                "freq_min_hz": extra.get("freq_min_hz"),
                "freq_max_hz": extra.get("freq_max_hz"),
                "noise_figure_db": extra.get("noise_figure_db"),
                "gain_db": extra.get("gain_db"),
                "iip3_dbm": extra.get("iip3_dbm"),
                "p1db_dbm": extra.get("p1db_dbm"),
                "screening_class": extra.get("screening_class", "industrial"),
                "temp_min_c": extra.get("temp_min_c", -40),
                "temp_max_c": extra.get("temp_max_c", 85),
                "package": extra.get("package", ""),
                "datasheet_url": extra.get("datasheet_url"),
                "datasheet_verified": False,
                "domain": domain,
                "key_specs": extra.get("key_specs", {}),
                "notes": "Expanded part — verify datasheet link before production use.",
            }
            # Strip None-valued numeric fields for cleanliness.
            entry = {k: v for k, v in entry.items() if v is not None}
            data["components"].append(entry)
            added += 1

        data.setdefault("_meta", {})["expanded"] = True
        data["_meta"]["parts_target_week1"] = max(
            data["_meta"].get("parts_target_week1", 20),
            len(data["components"]),
        )
        path.write_text(json.dumps(data, indent=2))
        total_after += len(data["components"])
        print(f"{domain:14s}: {len(existing_pns)} -> {len(data['components'])} (+{added})")

    print(f"\nTotal: {total_before} -> {total_after}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
